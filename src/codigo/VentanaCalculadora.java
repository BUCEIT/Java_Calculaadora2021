    private void BotonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonIgualActionPerformed
       //el boton igual tiene saber que operacion se pulso
       //como ese valor lo guarde en la variable operaacion
       //monto un if para que dependiendo del valor se realice รง
       //la operacion+
       double operando2 =Double.valueOf(display.getText());
        //si la operacion era la suma:
        if(operacion.equals("+")){
            operando1= operando1 + operando2;
        
         //si la operacion era resta
         if(operacion.equals("-")){
            operando1 = operando1 - operando2;
         }
         //si la operacion es multiplicaion
         if(operacion.equals("*")){
             operando1 =operando1 * operando2;
         }
         //si la operacion era division
         if(operacion.equals("/")){
             operando1 = operando1 / operando2;
         }
         
        }
        //por ultimo,muestro el resultado por pantall
        display.setText(String.valueOf(operando1));
    }//GEN-LAST:event_BotonIgualActionPerformed

